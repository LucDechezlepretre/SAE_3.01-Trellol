package trellol.trellol;

/**
 * Classe Importance servant à représenter tous les degrès d'importance
 * possible pour une tâche
 */
public class Importance {
    /**
     * Attribut statique représentant une importance faible
     */
    public static int FAIBLE = 0;
    /**
     * Attribut statique représentant une importance moyenne
     */
    public static int MOYENNE = 1;
    /**
     * Attribut statique représentant une importance élevée
     */
    public static int ELEVEE = 2;
}
